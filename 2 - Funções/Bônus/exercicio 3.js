function contagemDeFrutas (basket)
{
    let a = new Set(basket);
    
    let frutasCatalogadas = [...a], quantidadeDeFrutas = {};

    frutasCatalogadas.sort();

    for (let fruta of frutasCatalogadas)
    {
        let count = 0;

        for (let fruta2 of basket)
        {
            if (fruta === fruta2)
                count++;
        }

        quantidadeDeFrutas[fruta] = count;
    }
    
    return quantidadeDeFrutas;
}